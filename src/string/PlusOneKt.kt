import java.util.*
import kotlin.collections.ArrayList

class PlusOneKt {

    fun solve(k: Array<Int>): Array<Int> {

        val stack = Stack<Int>()
        var isUp = false

        k.reversed().forEachIndexed { index, i ->
            var input = i
            if (index == 0) {
                input++
            }
            if (isUp) {
                input++
            }
            if (input == 10) {
                isUp = true
                stack.add(0)
            } else {
                isUp = false
                stack.add(input)
            }
        }

        if (isUp) stack.add(1)

        return stack.toTypedArray().reversedArray()
    }

    fun solve2(k: Array<Int>): Array<Int> {
        val sb = StringBuffer()

        k.forEach {
            sb.append(it)
        }

        val value = ArrayList<Int>()

        val decimal = BigDecimal(sb.toString())
        decimal.plus(BigDecimal(1)).toString().toCharArray().forEach {
            value.add(it.digitToInt())
        }

        return value.toTypedArray()
    }
}

